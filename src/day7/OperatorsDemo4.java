package day7;

public class OperatorsDemo4 {

	public static void main(String[] args) {
		// Relational operator
		// > <,>=,<=,==, !=
		// Always give either true or false

		int a = 5;
		int b = 6;
		System.out.println(a > b); // false
		System.out.println(a < b); // true
		System.out.println(a >= b); // false
		System.out.println(a <= b); // true
		System.out.println(a != b); // true
		System.out.println(a == b); // false

		
	}

}
